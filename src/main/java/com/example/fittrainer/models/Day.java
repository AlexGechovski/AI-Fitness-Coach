package com.example.fittrainer.models;

public class Day {
    private int dayId;
    private String name;

    // Constructor, getters, and setters

    public int getDayId() {
        return dayId;
    }

    public void setDayId(int dayId) {
        this.dayId = dayId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
